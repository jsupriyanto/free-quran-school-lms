"use client";

import { useEffect, useState } from "react";

// Client-side only wrapper to prevent hydration issues
export default function ClientOnly({ children, fallback = null }) {
  const [mounted, setMounted] = useState(false);

  useEffect(() => {
    setMounted(true);
  }, []);

  if (!mounted) {
    return fallback;
  }

  return children;
}